#include "philo.h"

int	ft_atoi(const char *str)
{
	size_t			i;
	unsigned long	res;

	res = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			return (FALSE);
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return ((int)res);
}
